"""**Task 1-3: To clean the input word by removing non-word characters, white spaces, and digits exploiting "re"**"""

def clean_string(s):
    s = re.sub(r"[^\w\s]", '', s) # To replace all non-word characters (everything except numbers and letters) with no-space
    s = re.sub(r"\s+", '', s)     # To replace all runs of whitespaces with no-space
    s = re.sub(r"\d", '', s)      # To replace digits with no-space
    return s

"""**Task 1-4: To convert a list of tokenized sentences into a fixed-length numerical matrix by Zero-padding or Truncating each sentence to length seq_len (If review is shorter than seq_len, It is right-aligned, with zeros in the front. If review is longer than seq_len, It is truncated from the end, keeping the first seq_len tokens)**"""

def padding (sentences, seq_len):
    features = np.zeros((len(sentences), seq_len), dtype=int)
    for idx, review in enumerate(sentences): # This iterates over sentences matrix which includes several reviews (a list of numerical tokens)
        if len(review) != 0:                 # Ensures the sentence is not empty
            features[idx, -len(review):] = np.array(review)[:seq_len]
    return features

"""**Task 1-5: The following function cleans text, builds a limited vocabulary, converts sentences into integer token sequences, encodes labels, and pads sequences to a fixed length for use in next NLP models**"""

def tockenize(x_train, y_train, x_val, y_val, stop_words):

    word_list = []
    for sent in x_train:
        for word in sent.lower().split():
            word = clean_string(word)                 # First, it removes non-word characters, white spaces, and digits from each word of a sentence
            if word not in stop_words and word != '': # Second, it stores the word if it is not a stop_word or a no-space
                word_list.append(word)

    # This performs vocabulary construction for NLP preprocessing. It counts word frequencies and creates a corpus (a dictionary of word frequency),
    # selects the top 1000 most frequent words, and assigns each word a unique integer index for numerical representation.
    corpus = Counter(word_list)
    corpus_sorted = sorted(corpus, key=corpus.get, reverse=True)[:1000]
    one_hot_dict = {w:i+1 for i, w in enumerate(corpus_sorted)}
    print(corpus)
    print(corpus_sorted)
    print(one_hot_dict)

    # Tockenize converts a list of sentences (x_train) into a list of numerical indices, mapping words to their corresponding integer values in one_hot_dict
    # It is a common preprocessing step in Natural Language Processing (NLP) before feeding data into machine learning models
    final_list_train = []
    final_list_test  = []
    for sent in x_train:
        final_list_train.append([one_hot_dict[clean_string(word)] for word in sent.lower().split() if clean_string(word) in one_hot_dict.keys()])

    for sent in x_val:
        final_list_test.append([one_hot_dict[clean_string(word)] for word in sent.lower().split() if clean_string(word) in one_hot_dict.keys()])

    y_train_encoded = [1 if label =='positive' else 0 for label in y_train]
    y_test_encoded  = [1 if label =='positive' else 0 for label in y_val]

    # Padding to make all sequences of equal length
    max_length = 500
    x_train_padded = padding (final_list_train, max_length)
    x_test_padded  = padding (final_list_test,  max_length)

    return np.array(x_train_padded), np.array(y_train_encoded), np.array(x_test_padded), np.array(y_test_encoded), one_hot_dict

